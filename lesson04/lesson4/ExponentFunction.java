package lesson4;

public class ExponentFunction {
    public static void main(String[] args) {
        // Value of e
        System.out.println("Value of e = " + Math.E);

        // Displays the value of e squared
        System.out.println("Exponent of e = " + Math.exp(2.0));

        // Get the value of log(e)
        System.out.println("Value of log e = " + Math.log(Math.E));

        // Get the square root of 36
        System.out.println("The square root of 36 = " + Math.sqrt(36.0));


    }
}
